nn = int(input("informe um número: "))


if nn >=0 and nn <=25:
    print(f"o número {nn} esta na faixa de 0 a 25.")
elif nn >=26 and nn <=50:
    print(f"o número {nn} esta na faixa de 26 a 50.")
elif nn >=51 and nn <=75:
    print(f"o número {nn} esta na faixa de 51 a 75.")
elif nn >=76 and nn <=100:
    print(f"o número {nn} esta na faixa de 76 a 100.")